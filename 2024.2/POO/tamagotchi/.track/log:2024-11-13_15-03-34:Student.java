public class Student {
    private Game game; 
    public Student(int energy, int hungry, int clean) {
        this.game = new Game(new Pet(energy, hungry, clean));
    }

    public void show() {
        System.out.println(this.game.toString());
    }

    public void play() {
        this.game.play();
    }

    public void shower() {
        this.game.shower();
    }

    public void eat() {
        this.game.eat();
    }

    public void sleep() {
        this.game.sleep();
    }
}